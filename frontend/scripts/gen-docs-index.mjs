// gen-docs-index.mjs — derive the cloud docs index from docs/guide/usage.md.
//
// The Go server builds the local-mode docs sidebar by parsing the
// "## Reading Order" section of usage.md (parseReadingOrder in
// internal/cli/server.go). The cloud SPA cannot read the embedded FS at
// build time, so this script derives the same structure into
// frontend/src/data/docs.ts. usage.md stays the single source of truth;
// re-run this after editing its Reading Order:
//
//   node frontend/scripts/gen-docs-index.mjs
//
// A vitest (tests/docsIndex.test.ts) fails when the checked-in docs.ts
// drifts from usage.md.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../..');

// Mirror of the Go parser's rules: only the "## Reading Order" section is
// scanned; each "### " heading opens a section; every same-directory .md
// link becomes an entry. The sentence after the link (". Desc") becomes the
// card description for the cloud docs landing page.
export function parseReadingOrder(md) {
  const entries = [];
  let inSection = false;
  let section = '';
  for (const line of md.split('\n')) {
    const trimmed = line.trim();
    if (trimmed === '## Reading Order') {
      inSection = true;
      continue;
    }
    if (inSection && /^## /.test(trimmed)) break;
    if (!inSection) continue;
    const h = trimmed.match(/^### (.+)$/);
    if (h) {
      section = h[1].trim();
      continue;
    }
    const link = trimmed.match(/\[([^\]]+)\]\(([\w-]+)\.md\)\.?\s*(.*)$/);
    if (link) {
      entries.push({
        slug: link[2],
        title: link[1],
        section,
        desc: link[3].trim(),
      });
    }
  }
  return entries;
}

// usage.md is the index document itself: it never appears in its own
// Reading Order, but it is a real doc page, so it leads the index.
export const USAGE_ENTRY = {
  slug: 'usage',
  title: 'Usage Guide',
  section: 'Get Started',
  desc: 'Reading order and common workflows.',
};

export function renderDocsTs(entries) {
  const rows = [USAGE_ENTRY, ...entries]
    .map((e) => `  { slug: '${e.slug}', title: ${JSON.stringify(e.title)}, section: ${JSON.stringify(e.section)}, desc: ${JSON.stringify(e.desc)} },`)
    .join('\n');
  return `// GENERATED by frontend/scripts/gen-docs-index.mjs from docs/guide/usage.md.
// Do not edit by hand; edit the Reading Order in usage.md and re-run:
//   node frontend/scripts/gen-docs-index.mjs

export interface DocEntry {
  slug: string;
  title: string;
  section: string;
  desc: string;
}

export const docIndex: DocEntry[] = [
${rows}
];
`;
}

// Entry point (skipped when imported by tests).
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const md = readFileSync(resolve(repoRoot, 'docs/guide/usage.md'), 'utf8');
  const out = renderDocsTs(parseReadingOrder(md));
  writeFileSync(resolve(here, '../src/data/docs.ts'), out);
  console.log('wrote src/data/docs.ts');
}
